# Kullanıcıdan isim alınır
isim = input("İsminizi giriniz: ")

# Pozitif indeks ile karaktere erişim
print("7. karakter:", isim[6])

# Negatif indeks ile karaktere erişim
print("Sondan 3. karakter:", isim[-3])

# İlk 5 karakter
print("İlk 5 karakter:", isim[:5])

# Son 5 karakter
print("Son 5 karakter:", isim[-5:])

